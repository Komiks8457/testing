DWORD CRC32::s_CrcTable[CRC32_TABLE_SIZE][CRC32_TABLE_SIZE] = {0, };
BOOL  CRC32::s_CrcInitialized = FALSE;

void InitializeCrcLookupTable()
{
	for (int seed = 0; seed < CRC32_TABLE_SIZE; ++seed)
	{
		DWORD dwPolynomial = CRC32::s_Crc32Seed[seed];

		for (DWORD i = 0; i < CRC32_TABLE_SIZE; ++i)
		{
			DWORD crc = i;
			for (int j = 8; j > 0; --j)
			{
				if (crc & 0x00000001) 
					crc = (crc >> 1) ^ dwPolynomial;
				else
					crc >>= 1;
			}
			CRC32::s_CrcTable[seed][i] = crc;
		}
	}

	CRC32::s_CrcInitialized = TRUE;
}

CRC32::CRC32()
{
	Reset();
}

void CRC32::Initialize(DWORD dwSeed)
{
	m_dwTable	= dwSeed % CRC32_TABLE_SIZE;
	
	if (m_dwTable == 0)
		m_dwTable = 1;
}

BYTE CRC32::Generate(LPBYTE pData, DWORD dwLength)
{
	DWORD dwValue = Generate32( pData, dwLength );

	WORD wHID	= HIWORD(dwValue);
	WORD wLID	= LOWORD(dwValue);

	BYTE result	= HIBYTE(wHID) + LOBYTE(wHID) + HIBYTE(wLID) + LOBYTE(wLID);
	return result;
}

DWORD CRC32::Generate32(LPBYTE pData, DWORD dwLength)
{
	_ASSERT(CRC32::s_CrcInitialized == TRUE);
	_ASSERT(m_dwTable != 0);

	if (pData == NULL)
		return 0;

	DWORD dwValue = 0xFFFFFFFF;

	for (DWORD i = 0; i < dwLength; ++i)
		dwValue = (s_CrcTable[m_dwTable][((dwValue) ^ (*pData++)) & 0xFF] ^ (((dwValue) >> 8) & 0x00FFFFFF));

	return dwValue;
}
